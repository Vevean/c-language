#include<stdio.h>
#include<stdlib.h>
int main()
{
	int grade;
	scanf("%d",&grade);
	grade/=10;
	switch(grade){
	case 10:
	case 9:
		printf("%d",A);
		break;
	case 8:
		printf("%d",B);
		break;
	case 7:
		printf("%d",C);
		break;
	case 6:
		printf("%d",D);
		break;
	case 5:
		printf("%d",E);
		break;
	default :
		printf("%d",F);
		break;
	return 0;
	}
	 
}
